// 호수별 추천 제품 가져오기
export function getProductsByShade(shade) {
  return PRODUCTS_DB[shade] || PRODUCTS_DB['21']; // 기본값 21호
}

// 특정 타입 제품만 가져오기
export function getProductsByType(shade, type) {
  const products = getProductsByShade(shade);
  return products.filter(product => product.type === type);
}

// 추천 제품 조합 (쿠션 1 + 파데 1 + 컨실러 1)
export function getRecommendedProducts(shade) {
  const allProducts = getProductsByShade(shade);
  
  return {
    cushion: allProducts.find(p => p.type === '쿠션'),
    foundation: allProducts.find(p => p.type === '파운데이션'), 
    concealer: allProducts.find(p => p.type === '컨실러'),
    extra: allProducts.find(p => p.type === 'BB크림')
  };
}